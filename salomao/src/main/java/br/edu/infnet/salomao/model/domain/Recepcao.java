package br.edu.infnet.salomao.model.domain;

public class Recepcao extends Aluno {
    private String alunoDiario;
    private int quantidadeDiario;
    private String alunoSemanal;
    private int quantidadeSemanal;
    private String alunoMensal;
    private int quantidadeMensal;
    private String alunoAnual;
    private int quantidadeAnual;

    public String getAlunoDiario() {
        return alunoDiario;
    }

    public void setAlunoDiario(String alunoDiario) {
        this.alunoDiario = alunoDiario;
    }

    public int getQuantidadeDiario() {
        return quantidadeDiario;
    }

    public void setQuantidadeDiario(int quantidadeDiario) {
        this.quantidadeDiario = quantidadeDiario;
    }

    public String getAlunoSemanal() {
        return alunoSemanal;
    }

    public void setAlunoSemanal(String alunoSemanal) {
        this.alunoSemanal = alunoSemanal;
    }

    public int getQuantidadeSemanal() {
        return quantidadeSemanal;
    }

    public void setQuantidadeSemanal(int quantidadeSemanal) {
        this.quantidadeSemanal = quantidadeSemanal;
    }

    public String getAlunoMensal() {
        return alunoMensal;
    }

    public void setAlunoMensal(String alunoMensal) {
        this.alunoMensal = alunoMensal;
    }

    public int getQuantidadeMensal() {
        return quantidadeMensal;
    }

    public void setQuantidadeMensal(int quantidadeMensal) {
        this.quantidadeMensal = quantidadeMensal;
    }

    public String getAlunoAnual() {
        return alunoAnual;
    }

    public void setAlunoAnual(String alunoAnual) {
        this.alunoAnual = alunoAnual;
    }

    public int getQuantidadeAnual() {
        return quantidadeAnual;
    }

    public void setQuantidadeAnual(int quantidadeAnual) {
        this.quantidadeAnual = quantidadeAnual;
    }
}
